// materi if else if
var angka = 20;
if (angka % 2 === 0) {
  console.log(angka + ' adalah bilangan genap');
} else if (angka % 2 === 1) {
  console.log(angka + ' adalah bilangan ganjil');
}else {
  console.log(angka + ' adalah bukan angka');
}